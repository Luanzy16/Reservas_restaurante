    private void registrarseJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarseJBActionPerformed
        String opcion = JOptionPane.showInputDialog("Escribe 'cliente' si eres un cliente y 'empleado' si eres empleado");
        FactoryCuenta factory = new FactoryCuenta();
        Cuenta cuenta = factory.getCuenta(opcion);
        
        if(cuenta instanceof Cliente){
            cuenta.setNombreUsuario(this.nombreJT.getText());
            cuenta.setContrase単a(this.contrasenaJT.getText());
            cuenta.setEmail(this.emailJT.getText());
            cuenta.setId(Integer.parseInt(this.idJT.getText()));
            
             JOptionPane.showMessageDialog(null, cuenta.nuevaCuenta());
        }
        
        if(cuenta instanceof Empleado){
            String contrase = JOptionPane.showInputDialog("Ingresa la contrase単a para verificar si eres Empleado");
            
            if(contrase.equals(this.getContrasena())){
                cuenta.setNombreUsuario(this.nombreJT.getText());
                cuenta.setContrase単a(this.contrasenaJT.getText());
                cuenta.setEmail(this.emailJT.getText());
                cuenta.setId(Integer.parseInt(this.idJT.getText()));
            
                JOptionPane.showMessageDialog(null, cuenta.nuevaCuenta()); 
            }
              
        }      
        
        
    }//GEN-LAST:event_registrarseJBActionPerformed

